#include <unistd.h>

void	ft_print_comb(void)
{
	char i;
	char j;
	char k;
	
	i = '0';
	j = '1';
	k = '2';

	while (i <= '7')
	{
		while (j <= '8')
		{
			while (k <= '9')
			{
				if (i < j && j < k)
				{
					write (1, &i, 1);
					write (1, &j, 1);
					write (1, &k, 1);
					
					if (i != '7')
					{
						write (1, ", ", 2);			
					}
				}
				k++;	
			}
			j++;
			k = '2';
		}
		i++;
		j = '1';
	}
}
