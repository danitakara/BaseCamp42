#include <unistd.h>

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i++] != '\0')
	{
		write(1, &str[i - 1], 1);
	}
}

int 	main(void)
{
	char nome[6] = {'h','e','l','l','o'} ;
	ft_putstr(&nome[0]);
}